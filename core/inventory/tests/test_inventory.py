# ── Build Inventory ─────────────────────────────────────────────────

class TestBuildInventory:
    def test_basic_inventory(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main():\n    pass\n")
        (src / "utils.py").write_text("def helper(x):\n    return x\n")

        inv = build_inventory(str(src), str(tmp_path / "out"))

        assert inv["total_files"] == 2
        assert inv["total_functions"] == 2
        assert (tmp_path / "out" / "checklist.json").exists()

    def test_sha256_present(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")

        inv = build_inventory(str(src), str(tmp_path / "out"))
        assert inv["files"][0]["sha256"]
        assert len(inv["files"][0]["sha256"]) == 64  # SHA-256 hex length

    def test_checked_by_is_list(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")

        inv = build_inventory(str(src), str(tmp_path / "out"))
        func = inv["files"][0]["functions"][0]
        assert isinstance(func["checked_by"], list)
        assert func["checked_by"] == []

    def test_exclusion_recording(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        tests_dir = src / "tests"
        tests_dir.mkdir()
        (src / "app.py").write_text("def main(): pass\n")
        (tests_dir / "test_app.py").write_text("def test_main(): pass\n")

        inv = build_inventory(str(src), str(tmp_path / "out"))

        assert inv["total_files"] == 1  # Only app.py
        assert len(inv["excluded_files"]) >= 1
        excluded_paths = [e["path"] for e in inv["excluded_files"]]
        assert any("test_app.py" in p for p in excluded_paths)

    def test_generated_file_excluded(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "real.py").write_text("def real(): pass\n")
        (src / "gen.py").write_text("# auto-generated by tool\ndef gen(): pass\n")

        inv = build_inventory(str(src), str(tmp_path / "out"))

        assert inv["total_files"] == 1
        file_paths = [f["path"] for f in inv["files"]]
        assert "real.py" in file_paths
        excluded_reasons = [e["reason"] for e in inv["excluded_files"]]
        assert "generated_file" in excluded_reasons

    def test_multi_language(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")
        (src / "main.c").write_text("int main(void) {\n    return 0;\n}\n")
        (src / "app.js").write_text("function init() {\n}\n")

        inv = build_inventory(str(src), str(tmp_path / "out"))

        languages = {f["language"] for f in inv["files"]}
        assert "python" in languages
        assert "c" in languages
        assert "javascript" in languages

    def test_nonexistent_target_raises(self, tmp_path):
        with pytest.raises(FileNotFoundError):
            build_inventory("/nonexistent/path", str(tmp_path / "out"))


# ── Compare Inventories ─────────────────────────────────────────────

class TestCompareInventories:
    def test_identical_returns_none(self):
        inv = {"files": [{"path": "a.py", "sha256": "abc"}]}
        assert compare_inventories(inv, inv) is None

    def test_detects_added(self):
        old = {"files": [{"path": "a.py", "sha256": "abc"}]}
        new = {"files": [
            {"path": "a.py", "sha256": "abc"},
            {"path": "b.py", "sha256": "def"},
        ]}
        diff = compare_inventories(old, new)
        assert "b.py" in diff["added"]

    def test_detects_removed(self):
        old = {"files": [
            {"path": "a.py", "sha256": "abc"},
            {"path": "b.py", "sha256": "def"},
        ]}
        new = {"files": [{"path": "a.py", "sha256": "abc"}]}
        diff = compare_inventories(old, new)
        assert "b.py" in diff["removed"]

    def test_detects_modified(self):
        old = {"files": [{"path": "a.py", "sha256": "abc"}]}
        new = {"files": [{"path": "a.py", "sha256": "xyz"}]}
        diff = compare_inventories(old, new)
        assert "a.py" in diff["modified"]


# ── Cumulative Coverage ─────────────────────────────────────────────

class TestCumulativeCoverage:
    def test_carry_forward_unchanged(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")

        out = tmp_path / "out"

        # First run
        inv1 = build_inventory(str(src), str(out))
        # Simulate coverage
        inv1["files"][0]["functions"][0]["checked_by"] = ["validate:stage-a"]
        with open(out / "checklist.json", "w") as f:
            json.dump(inv1, f)

        # Second run (same source)
        inv2 = build_inventory(str(src), str(out))
        assert inv2["files"][0]["functions"][0]["checked_by"] == ["validate:stage-a"]

    def test_clear_modified_file(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")

        out = tmp_path / "out"

        # First run
        inv1 = build_inventory(str(src), str(out))
        inv1["files"][0]["functions"][0]["checked_by"] = ["validate:stage-a"]
        with open(out / "checklist.json", "w") as f:
            json.dump(inv1, f)

        # Modify source
        (src / "app.py").write_text("def main():\n    print('changed')\n")

        # Second run
        inv2 = build_inventory(str(src), str(out))
        assert inv2["files"][0]["functions"][0]["checked_by"] == []

    def test_new_file_empty_coverage(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")

        out = tmp_path / "out"

        # First run
        build_inventory(str(src), str(out))

        # Add new file
        (src / "new.py").write_text("def new_func(): pass\n")

        # Second run
        inv2 = build_inventory(str(src), str(out))
        new_file = next(f for f in inv2["files"] if f["path"] == "new.py")
        assert new_file["functions"][0]["checked_by"] == []


# ── Coverage Stats ──────────────────────────────────────────────────

class TestCoverageStats:
    def test_basic_stats(self):
        inv = {
            "files": [{
                "path": "app.py",
                "functions": [
                    {"name": "foo", "checked_by": ["validate:stage-a"]},
                    {"name": "bar", "checked_by": []},
                    {"name": "baz", "checked_by": ["validate:stage-a", "understand:map"]},
                ],
            }]
        }
        stats = get_coverage_stats(inv)
        assert stats["total_functions"] == 3
        assert stats["checked_functions"] == 2
        assert stats["by_source"]["validate:stage-a"] == 2
        assert stats["by_source"]["understand:map"] == 1

    def test_empty_inventory(self):
        stats = get_coverage_stats({"files": []})
        assert stats["total_functions"] == 0
        assert stats["coverage_percent"] == 0

    def test_update_coverage_appends(self):
        inv = {
            "files": [{
                "path": "app.py",
                "functions": [
                    {"name": "foo", "checked_by": []},
                ],
            }]
        }
        update_coverage(inv, [{"file": "app.py", "function": "foo"}], "validate:stage-a")
        assert "validate:stage-a" in inv["files"][0]["functions"][0]["checked_by"]

    def test_update_coverage_deduplicates(self):
        inv = {
            "files": [{
                "path": "app.py",
                "functions": [
                    {"name": "foo", "checked_by": ["validate:stage-a"]},
                ],
            }]
        }
        update_coverage(inv, [{"file": "app.py", "function": "foo"}], "validate:stage-a")
        assert inv["files"][0]["functions"][0]["checked_by"].count("validate:stage-a") == 1

    def test_format_summary(self):
        inv = {
            "total_files": 10,
            "excluded_files": [{"path": "t.py", "reason": "test"}],
            "files": [{
                "path": "app.py",
                "functions": [
                    {"name": "foo", "checked_by": ["validate:stage-a"]},
                    {"name": "bar", "checked_by": []},
                ],
            }],
        }
        summary = format_coverage_summary(inv)
        assert "10 files" in summary
        assert "1 excluded" in summary
        assert "1/2" in summary


# ── CLI Script ──────────────────────────────────────────────────────

class TestCLIScript:
    def test_build_inventory_script(self, tmp_path):
        src = tmp_path / "src"
        src.mkdir()
        (src / "app.py").write_text("def main(): pass\n")

        result = subprocess.run(
            [sys.executable, "build_inventory.py", "--repo", str(src), "--out", str(tmp_path / "out")],
            capture_output=True, text=True, timeout=30,
        )
        assert result.returncode == 0
        assert "Inventory:" in result.stdout
        assert (tmp_path / "out" / "checklist.json").exists()

    def test_nonexistent_repo_fails(self, tmp_path):
        result = subprocess.run(
            [sys.executable, "build_inventory.py", "--repo", "/nonexistent", "--out", str(tmp_path)],
            capture_output=True, text=True, timeout=30,
        )
        assert result.returncode == 1
