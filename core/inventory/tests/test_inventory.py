# ── Exclusions ──────────────────────────────────────────────────────

class TestExclusions:
    def test_directory_pattern(self):
        assert should_exclude("project/tests/test_foo.py", ["tests/"]) is True
        assert should_exclude("project/src/foo.py", ["tests/"]) is False

    def test_glob_pattern(self):
        assert should_exclude("test_foo.py", ["test_*"]) is True
        assert should_exclude("foo_test.py", ["*_test.*"]) is True
        assert should_exclude("foo.py", ["test_*"]) is False

    def test_vendor_excluded(self):
        assert should_exclude("vendor/lib/foo.js", DEFAULT_EXCLUDES) is True
        assert should_exclude("node_modules/pkg/index.js", DEFAULT_EXCLUDES) is True

    def test_binary_detection(self, tmp_path):
        binary = tmp_path / "test.bin"
        binary.write_bytes(b'\x00\x01\x02\x03')
        assert is_binary_file(binary) is True

        text = tmp_path / "test.txt"
        text.write_text("hello world")
        assert is_binary_file(text) is False

    def test_generated_file_detection(self):
        assert is_generated_file("# auto-generated by protoc\ncode here") is True
        assert is_generated_file("# DO NOT EDIT\ncode here") is True
        assert is_generated_file("# my cool module\ndef foo(): pass") is False

    def test_match_exclusion_reason(self):
        excluded, reason, pattern = match_exclusion_reason(
            "project/tests/test_foo.py", ["tests/"]
        )
        assert excluded is True
        assert reason == "pattern_match"
        assert pattern == "tests/"

    def test_match_exclusion_not_excluded(self):
        excluded, reason, pattern = match_exclusion_reason(
            "src/app.py", ["tests/"]
        )
        assert excluded is False
        assert reason is None


